function setup(){
    createCanvas(1000, 1000);
    background(255);
}

function draw(){

    strokeWeight(5);

    // Syntax: line(x1, y1, x2, y2)
    line(800, 100, 300, 200);
    line(500, 100, 250, 500);
    line(300, 300, 250, 700);
    line(200, 600, 400, 1000);
    line(1000, 400, 700, 100);
    line(800, 700, 600, 100);
    line(600, 400, 300, 300);
    line(1000, 300, 600, 100);
    line(200, 800, 900, 1000);
    line(600, 400, 500, 100);
    line(900, 400, 700, 1000);
    line(800, 900, 1000, 400);
    line(300, 300, 1000, 400);
    line(300, 200, 400, 400);
    line(400, 900, 1000, 800);
}